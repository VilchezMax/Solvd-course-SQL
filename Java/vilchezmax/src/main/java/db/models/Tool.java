package db.models;

import db.MySQLDAO;
import db.dao.IToolDAO;

public class Tool  extends MySQLDAO implements IToolDAO {
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
